public class Rol {
    private String ID_Rol;
    private String Rol;


    public Rol(String ID_Rol, String Rol) {
        this.ID_Rol = ID_Rol;
        this.Rol = Rol;
    }

    public String getID_Rol() {
        return ID_Rol;
    }

    public void setID_Rol(String ID_Rol) {
        this.ID_Rol = ID_Rol;
    }

    public String getRol() {
        return Rol;
    }

    public void setRol(String Rol) {
        this.Rol = Rol;
    }
}
